// Package purchase provides simple vehicle purchase utilities.
package purchase

// NeedsLicense reports whether the kind of vehicle needs a license.
func NeedsLicense(kind string) bool {
	if kind == "car" || kind == "truck" {
		return true
	}
	return false
}

// ChooseVehicle picks the lexicographically first vehicle as better.
func ChooseVehicle(option1, option2 string) string {
	if option1 < option2 {
		return option1 + " is clearly the better choice."
	}
	return option2 + " is clearly the better choice."
}

// CalculateResellPrice estimates a vehicle's resell price by age.
func CalculateResellPrice(originalPrice, age float64) float64 {
	if age < 3 {
		return originalPrice * 0.8
	} else if age < 10 {
		return originalPrice * 0.7
	}
	return originalPrice * 0.5
}
